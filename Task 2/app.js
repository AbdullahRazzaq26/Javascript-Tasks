var userInput = prompt ("Enter The Weather Condition (Sunny,Rainy,Snowy)")

if(userInput == "sunny" ){
    alert("Wear sunglasses!")
    console.log("Wear sunglasses!");    
}
else if(userInput == "rainy"){
    alert("Take an umbrella")
    console.log("Take an umbrella");    
}
else if(userInput == "snowy"){
    alert("Wear warm clothes.")
    console.log("Wear warm clothes.");    
}
else{
    alert("Enjoy your day!")
    console.log("Enjoy your day!");    
}