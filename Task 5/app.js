var userInput = prompt ("Enter The Number")

if(userInput >= 1 && userInput <= 10){
    alert("The number is between 1 and 10.")
    console.log("The number is between 1 and 10.");    
}
else if(userInput >= 10 && userInput <= 20){
    alert("The number is between 11 and 20.")
    console.log("The number is between 11 and 20.");    
}
else if(userInput >= 20 && userInput <= 30){
    alert("The number is between 21 and 30.")
    console.log("The number is between 11 and 20.");    
}
else if(userInput >= 30 && userInput <= 40){
    alert("The number is between 31 and 40.")
    console.log("The number is between 11 and 20.");    
}
else if(userInput >= 40 && userInput <= 50){
    alert("The number is between 41 and 50.")
    console.log("The number is between 11 and 20.");    
}
else{
    alert("The number is out of range.")
    console.log("The number is out of range.");    
}